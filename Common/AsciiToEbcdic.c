void
AsciiToEbcdic( unsigned char * buf, int len )
{
  int	i;
  int	end;

  if( len == 0 )
    {
      end = strlen( (char *)buf );
    }
  else
    {
      end = len;
    }

  for( i = 0; i < end; i++, buf++ )
    {
      *buf = AtoE[*buf];
    }
}
